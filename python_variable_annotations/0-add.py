#!/usr/bin/python3
"""
Function take argument float and return float.
"""


def add(a: float, b: float) -> float:
    """
    Function to add two float and return a float.

    Args:
        a (float): First float argument
        b (float): Second float argument

    Returns:
        float: Return the sum of two float in type float
    """
    return a + b
