#!/usr/bin/python
"""
Concat two string.
"""


def concat(str1: str, str2: str) -> str:
    """
    Function to concat two string and return a string object

    Args:
        str1 (str): First string to concat
        str2 (str): Second string to concat

    Returns:
        str: Return the resul after concat the two string in string object
    """
    return str1 + str2
